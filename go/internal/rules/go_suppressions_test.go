func TestSuppressionFindingReportsFirstOffensePerFile(t *testing.T) {
	findings := suppressionFindings(t, map[string]repo.File{
		"a.go": goFile("a.go",
			"package a",
			"var x = f() //nolint:errcheck",
			"var y = g() //nolint:errcheck",
		),
		"b.go": goFile("b.go",
			"package b",
			"var z = h() //nolint:errcheck",
		),
	})

	if len(findings) != 2 {
		t.Fatalf("findings = %#v, want two", findings)
	}
	if findings[0].Path != "a.go" || !strings.Contains(findings[0].Message, "(line 2)") {
		t.Fatalf("finding = %#v", findings[0])
	}
	if findings[1].Path != "b.go" {
		t.Fatalf("finding = %#v", findings[1])
	}
}

func TestNonProductionPathsAreExempt(t *testing.T) {
	paths := []string{
		"internal/app_test.go",
		"fixtures/repos/app.go",
		"templates/go/app.go",
		"internal/testdata/app.go",
		"vendor/dep/app.go",
		"scripts/tool.go",
		"internal/generated/app.go",
	}
	files := map[string]repo.File{}
	for _, path := range paths {
		files[path] = goFile(path, "package x", "var x = f() //nolint:errcheck")
	}

	if findings := suppressionFindings(t, files); len(findings) != 0 {
		t.Fatalf("findings = %#v, want none", findings)
	}
}

