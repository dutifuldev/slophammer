package rules

import (
	"context"
	"strings"
	"testing"

	"github.com/dutifuldev/slophammer/go/internal/repo"
)

func suppressionFindings(t *testing.T, files map[string]repo.File) []Finding {
	t.Helper()
	rule := newGoSuppressionsRule(definitionByID(t, GoSuppressionsJustifiedRuleID))
	return rule.Check(context.Background(), repo.NewSnapshot("/repo", files))
}

func goFile(path string, lines ...string) repo.File {
	return repo.File{Path: path, Content: strings.Join(lines, "\n") + "\n"}
}

func TestBareNolintInProductionCodeIsAFinding(t *testing.T) {
	findings := suppressionFindings(t, map[string]repo.File{
		"internal/app.go": goFile("internal/app.go",
			"package app",
			"func run() error { return nil } //nolint:errcheck",
		),
	})

	if len(findings) != 1 {
		t.Fatalf("findings = %#v, want one", findings)
	}
	if findings[0].Path != "internal/app.go" || !strings.Contains(findings[0].Message, "(line 2)") {
		t.Fatalf("finding = %#v", findings[0])
	}
}

func TestJustifiedSuppressionsPass(t *testing.T) {
	findings := suppressionFindings(t, map[string]repo.File{
		"internal/app.go": goFile("internal/app.go",
			"package app",
			"func run() error { return nil } //nolint:errcheck // exit path already reported",
			"// the linter cannot see the build tag context here",
			"var fallback = legacy() //nolint:staticcheck",
		),
	})

	if len(findings) != 0 {
		t.Fatalf("findings = %#v, want none", findings)
	}
}

